# calculation of age for 10 years and 20 years
current_age =  int(input('Enter current age: '))
age_in_ten_years = current_age + 10
age_in_twenty_years = current_age + 20
print(f"You will be {age_in_ten_years} years old in 10 years time") 
print(f"You will be {age_in_twenty_years} years old in 20 years time")
